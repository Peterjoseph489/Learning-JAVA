
public class Expression {

	public static void main(String[] args) {
		
		
		// expression = operands & operators
		// operands = Values, variables, numbers, quantity
		// operators = + - * / %
		
		
		int friends = 10;
		
		friends = friends + 1;
		
		System.out.println(friends);
		
		friends++;
		System.out.println(friends);
		
		friends--;
		System.out.println(friends);
		
		
	}
}
